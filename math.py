# add
def add(a,b):
    answer = a+b
    return answer


# subtract
def subtract(x,y):
    subtraction = x-y
    return subtraction

 # divide
def divide(r,s):
    division = r/s
    return division

# multiply
def multiply(c,d):
    multiplication = c*d
    return multiplication
 
#  remainder
def remainder(e,f):
    modulus = e%f
    return modulus